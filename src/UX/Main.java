    private void ButtonImagenMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonImagenMousePressed
        try{
        JFileChooser file = new JFileChooser();
        file.addChoosableFileFilter(new FileNameExtensionFilter("Images", "jpg", "png", "gif", "bmp"));
        file.setAcceptAllFileFilterUsed(false);
        file.showOpenDialog(file);
        
            File abre = file.getSelectedFile();
        String rutaConstancia = abre.getAbsolutePath();
        

        File ruta = new File("C:/izcaliCourses/constancesPath.izd");
        if (!ruta.exists()) {
            try {
                ruta.createNewFile();
            } catch (IOException ex) {

            }
        }

        try {
            FileWriter fw = new FileWriter(ruta);
            fw.write(rutaConstancia);
            JOptionPane.showMessageDialog(null, "Archivo cargado exitosamente " + rutaConstancia);
            fw.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error CODE 395");
        }
        }catch(Exception err){}
    }//GEN-LAST:event_ButtonImagenMousePressed
    public void getCurrent() {
        File currentInfo = new File("C:/izcaliCourses/current.izd");
        try {
            FileReader fr = new FileReader(currentInfo);
            BufferedReader br = new BufferedReader(fr);
            
            StringTokenizer st = new StringTokenizer(br.readLine(), "*");
            CurrentType.setText(st.nextToken());
            CurrentName.setText(st.nextToken());
            CurrentDate.setText(st.nextToken());

            br.close();
            fr.close();

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "File not Founded");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error in the readable sentence");
        }

    }

