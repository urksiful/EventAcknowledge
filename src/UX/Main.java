    private void ChangeButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ChangeButtonMousePressed
        if (!TypeCOMBO.getSelectedItem().toString().equals("Select any Item") && !CourseField.getText().equals("") && !CourseDay.getSelectedItem().toString().equals("DD") && !CourseMonth.getSelectedItem().toString().equals("MM") && !CourseYear.getSelectedItem().toString().equals("YYYY")) {
            Scripts.SaveCourseData save = new Scripts.SaveCourseData();
            save.setDate(CourseDay.getSelectedItem().toString(), CourseMonth.getSelectedItem().toString(), CourseYear.getSelectedItem().toString());
            save.nameCourse = CourseField.getText();
            save.type = TypeCOMBO.getSelectedItem().toString();
            save.saveDataCourse();
            getCurrent();
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona todos los campos");
        }

    }//GEN-LAST:event_ChangeButtonMousePressed

